  methods: {
    updateSection1Carousel(bgIndex) {
      this.activeIndex = bgIndex;

      let carouselContainer = this.$refs["section-1-carousel-container"];

      carouselContainer.scrollLeft =
        (carouselContainer.scrollWidth * bgIndex) /
        this.section1CarouselItems.length;
    },
  },
};
</script>

